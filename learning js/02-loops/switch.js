let price;

switch (stars) { 
    case 1: // ---> когда stars равно 1 
        price = 20;
        break;
    case 2:
        price = 30;
        break;
    case 3:
        price = 50;
        break;
    case 4:
        price = 70;
        break;
    case 5:
        price = 120;
        break;
    default: 
       console.log('Такого кол-ва звезд нет'); 
}
console.log(price);

