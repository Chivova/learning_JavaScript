//     ----> тело <---- 
//     case : - это случаи (после бробела ставится значение с которым вы хотите сравнить)
// ----> тело case ; <----
//     break ; - оператор (инструкця) преривания, прерывает case когда он выполняется, что бы не шло дальше
    //  default : ----> значение по-умолчанию (когда не одно из case не выполняеться), break после него ставить не нужно
// }

